def add_table(b, headers, rows, col_widths=None, bold_last=False):
    doc = b.doc
    table = doc.add_table(rows=1 + len(rows), cols=len(headers))
    table.style = "Table Grid"
    hrow = table.rows[0]
    for i, h in enumerate(headers):
        cell = hrow.cells[i]
        cell.paragraphs[0].clear()
        run = cell.paragraphs[0].add_run(h)
        run.font.name = FONT; run.font.size = Pt(11); run.font.bold = True
        cell.paragraphs[0].paragraph_format.space_before = Pt(2)
        cell.paragraphs[0].paragraph_format.space_after = Pt(2)
        _shade_cell(cell, "D9D9D9")
    for ri, row in enumerate(rows):
        trow = table.rows[ri + 1]
        is_last = (ri == len(rows) - 1)
        for ci, val in enumerate(row):
            cell = trow.cells[ci]
            cell.paragraphs[0].clear()
            run = cell.paragraphs[0].add_run(str(val))
            run.font.name = FONT; run.font.size = Pt(10)
            run.font.bold = (bold_last and is_last)
            if bold_last and is_last:
                _shade_cell(cell, "F2F2F2")
            cell.paragraphs[0].paragraph_format.space_before = Pt(2)
            cell.paragraphs[0].paragraph_format.space_after = Pt(2)
    if col_widths:
        tbl = table._tbl
        tblPr = tbl.find(qn("w:tblPr"))
        if tblPr is None:
            tblPr = OxmlElement("w:tblPr"); tbl.insert(0, tblPr)
        tblW = OxmlElement("w:tblW")
        tblW.set(qn("w:w"), "0"); tblW.set(qn("w:type"), "auto")
        tblPr.append(tblW)
        tblGrid = OxmlElement("w:tblGrid")
        for w in col_widths:
            col = OxmlElement("w:gridCol"); col.set(qn("w:w"), str(w))
            tblGrid.append(col)
        tbl.insert(1, tblGrid)
    doc.add_paragraph()


def _shade_cell(cell, fill_hex):
    tc = cell._tc
    tcPr = tc.find(qn("w:tcPr"))
    if tcPr is None:
        tcPr = OxmlElement("w:tcPr"); tc.insert(0, tcPr)
    shd = OxmlElement("w:shd")
    shd.set(qn("w:val"), "clear"); shd.set(qn("w:color"), "auto")
    shd.set(qn("w:fill"), fill_hex)
    tcPr.append(shd)


